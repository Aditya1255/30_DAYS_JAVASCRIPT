/*    String + Number = String
      String - Number = Number
      String / Number = NUmber
*/


let x = 10;     //Number
let y = '5';    //String

console.log(x + y);   //Number + String = string.

console.log(x - y);   //Number - String = Number.

console.log(x * y);   //Number * String = Number.

console.log(x / y);   //Number / String = Number.